CREATE DATABASE PMSCOMPLETE;
USE PMSCOMPLETE;

CREATE TABLE PMS_DEPARTMENT_DETAILS
(
DEPARTMENT_ID INT8 PRIMARY KEY, 
DEPARTMENT_NAME VARCHAR(30)NOT NULL,
DEPARTMENT_LOCATION VARCHAR(30)NOT NULL,
DEPARTMENT_EXTENSION INT8 NOT NULL
);

CREATE TABLE PMS_MANAGER_DETAILS
(
Manager_ID INT8 PRIMARY KEY,
Manager_Name VARCHAR(30) NOT NULL,
Job VARCHAR(30) NOT NULL,
Boss_Code INT8,
Salary INT8 NOT NULL,
Commission INT8,
DEPARTMENT_ID INT8
); 

CREATE TABLE PMS_UNIT_DETAILS
(
UNIT_ID VARCHAR(2) PRIMARY KEY,
UNIT_NAME VARCHAR(30) NOT NULL,
PIECE_WEIGHT VARCHAR(15) NOT NULL,
TOTAL_PIECES INT8 NOT NULL,
UNIT_WEIGHT INT8 NOT NULL
);

CREATE TABLE PMS_PRODUCT
(
PRODUCT_ID VARCHAR(5) PRIMARY KEY,
PRODUCT_NAME VARCHAR(30) NOT NULL,
DEPARTMENT_ID INT8
);

CREATE TABLE PMS_PRODUCT_UNIT
(
PRODUCT_ID VARCHAR(5),
UNIT_ID VARCHAR(2)
);

CREATE TABLE PMS_MANUFACTURING
(
MANFATURE_ID VARCHAR(5) PRIMARY KEY,
PRODUCT_ID VARCHAR(5) NOT NULL,
UNIT_ID VARCHAR(5) NOT NULL,
QUANTITY INT8 NOT NULL,
AVAILABILITY VARCHAR(3) NOT NULL,
PRODUCT_MANFACTURE_DATE DATE NOT NULL,
PRODUCT_EXPIRY_DATE DATE NOT NULL);

ALTER TABLE PMS_MANAGER_DETAILS ADD constraint FK_DEPARTMENT_ID1 foreign key(DEPARTMENT_ID)
references PMS_DEPARTMENT_DETAILS(DEPARTMENT_ID);

ALTER TABLE PMS_PRODUCT ADD constraint FK_DEPARTMENT_ID2 foreign key(DEPARTMENT_ID) references
PMS_DEPARTMENT_DETAILS(DEPARTMENT_ID);

ALTER TABLE PMS_PRODUCT_UNIT ADD constraint PK_Composite21 primary key(PRODUCT_ID,UNIT_ID);

ALTER TABLE PMS_PRODUCT_UNIT ADD constraint FK_PRODUCT_ID1 foreign key(PRODUCT_ID) references
PMS_PRODUCT(PRODUCT_ID);

ALTER TABLE PMS_PRODUCT_UNIT ADD constraint FK_UNIT_ID1 foreign key(UNIT_ID) references
PMS_UNIT_DETAILS(UNIT_ID);

ALTER TABLE PMS_MANUFACTURING ADD constraint FK_PRODUCT_ID2 foreign key(PRODUCT_ID) references
PMS_PRODUCT(PRODUCT_ID);

ALTER TABLE PMS_MANUFACTURING ADD constraint FK_UNIT_ID2 foreign key(UNIT_ID) references
PMS_UNIT_DETAILS(UNIT_ID);

